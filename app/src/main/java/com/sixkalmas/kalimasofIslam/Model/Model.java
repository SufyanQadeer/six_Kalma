package com.sixkalmas.kalimasofIslam.Model;

public class Model {

    String kalmaName;
    String kalmaArabic;
    String kalmaUrdu;
    String kalmaEnglish;
    String kalmaAudio;
    String kalmaRoman;

    public Model() {
    }


    public String getKalmaName() {
        return kalmaName;
    }

    public void setKalmaName(String kalmaName) {
        this.kalmaName = kalmaName;
    }

    public String getKalmaArabic() {
        return kalmaArabic;
    }

    public void setKalmaArabic(String kalmaArabic) {
        this.kalmaArabic = kalmaArabic;
    }

    public String getKalmaUrdu() {
        return kalmaUrdu;
    }

    public void setKalmaUrdu(String kalmaUrdu) {
        this.kalmaUrdu = kalmaUrdu;
    }

    public String getKalmaEnglish() {
        return kalmaEnglish;
    }

    public void setKalmaEnglish(String kalmaEnglish) {
        this.kalmaEnglish = kalmaEnglish;
    }

    public String getKalmaAudio() {
        return kalmaAudio;
    }

    public void setKalmaAudio(String kalmaAudio) {
        this.kalmaAudio = kalmaAudio;
    }

    public String getKalmaRoman() {
        return kalmaRoman;
    }

    public void setKalmaRoman(String kalmaRoman) {
        this.kalmaRoman = kalmaRoman;
    }
}
